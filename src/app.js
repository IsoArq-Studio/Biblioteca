  function resolveSectionId(file, folder) {
    const raw = [file.category, folder.category, folder.name, file.folder, file.name, file.title, file.mimeType, file.tags].filter(Boolean).join(' ');
    const slug = normalizeText(raw);

    if (shouldSkipResource(slug)) return null;
    if (isAutoCadResource(slug, file)) return classifyCad(slug);

    const alias = sectionAliases[normalizeText(file.category || folder.category || folder.name)];
    if (alias) return alias;

    for (const group of categoryWords) {
      if (hasAny(slug, group.words)) return group.id;
    }

    return 'arquitectura-general';
  }

